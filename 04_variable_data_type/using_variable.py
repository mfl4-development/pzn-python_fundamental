# using variables in operations
first_name = "Stella"
middle_name = "Marie"
last_name = "Johnson"
full_name = first_name + " " + middle_name + " " + last_name

length = 10
width = 5
area = length * width

# using variables in print statements
print("Full Name:", full_name)  # output: Full Name: Stella Marie Johnson
print("Area of Rectangle:", area)  # output: Area of Rectangle: 50
